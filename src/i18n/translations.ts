/** Resolve locale with fallback chain: exact → language prefix → en-US */
export function t(locale: string, key: keyof VideoStrings): string {
  const direct = translations[locale]?.[key];
  if (typeof direct === "string" && direct.length > 0) return direct;
  const prefix = locale.split("-")[0];
  const fallback = Object.keys(translations).find((k) =>
    k.startsWith(prefix)
  );
  if (fallback) {
    const fb = translations[fallback][key];
    if (typeof fb === "string" && fb.length > 0) return fb;
  }
  return translations["en-US"][key] ?? "";
}

const BADGE_SEP = " · ";

/** Three step labels from `s3_badge` / `s4_badge` (middle-dot separated). */
export function badgeTriple(
  locale: string,
  key: "s3_badge" | "s4_badge"
): readonly [string, string, string] {
  const raw = t(locale, key);
  const parts = raw.split(BADGE_SEP).map((p) => p.trim()).filter(Boolean);
  if (parts.length >= 3) {
    return [parts[0], parts[1], parts[2]];
  }
  const enRaw = translations["en-US"][key];
  const enParts = enRaw.split(BADGE_SEP).map((p) => p.trim()).filter(Boolean);
  return [
    parts[0] ?? enParts[0] ?? "",
    parts[1] ?? enParts[1] ?? "",
    parts[2] ?? enParts[2] ?? "",
  ];
}

